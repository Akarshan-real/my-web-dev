(function hoverColorChange() {
    {
        document.querySelectorAll(".blueLogoGlow").forEach(e => {
            e.addEventListener("mouseenter",()=>{
                e.style.color = "rgb(29,155,240)";
            });
            e.addEventListener("mouseleave",()=>{
                e.style.color = "rgb(113,118,123)";
            });
        })
    }
})();

